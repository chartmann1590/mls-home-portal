def _heuristic_home_summary(home: Listing, effective_cap: int) -> tuple[str, bool, str]:
    pct = (home.price / effective_cap) if effective_cap else 1.0
    if pct <= 0.85:
        fit = True
        reason = "Comfortably below your effective cap."
    elif pct <= 1.0:
        fit = True
        reason = "Within budget, but tighter monthly flexibility."
    else:
        fit = False
        reason = "Over your effective budget cap."
    summary = (
        f"{home.address}: {home.beds if home.beds is not None else '?'} bd, "
        f"{home.baths if home.baths is not None else '?'} ba, "
        f"{home.sqft if home.sqft is not None else '?'} sqft."
    )
    return summary, fit, reason


