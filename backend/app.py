async def _run_search(req: SearchRequest) -> SearchResponse:
    profile = {
        "annual_income": req.annual_income,
        "monthly_debt": req.monthly_debt,
        "down_payment": req.down_payment,
        "interest_rate": req.interest_rate,
        "loan_term_years": req.loan_term_years,
        "requested_max_price": req.max_price,
        "area": req.area,
    }

    ai = await ollama_client.affordability_estimate(profile)
    notes: List[str] = []
    if ai is None:
        ai = heuristic_affordability(
            annual_income=req.annual_income,
            monthly_debt=req.monthly_debt,
            down_payment=req.down_payment,
            interest_rate=req.interest_rate,
            loan_term_years=req.loan_term_years,
        )
        notes.append("Ollama unavailable or returned invalid JSON. Used heuristic affordability model.")
    else:
        notes.append("Affordability generated by Ollama.")

    affordability = AffordabilityResult(**ai)
    effective_cap = min(req.max_price, affordability.max_affordable_home_price)

    raw_listings, listing_notes = await listing_service.search(req.area, effective_cap, req.provider)
    notes.extend(listing_notes)

    listings = [Listing(**l) for l in raw_listings if l.get("price", 10**9) <= effective_cap]
    if listings:
        sources = sorted({x.source for x in listings})
        notes.append(f"Listing sources: {', '.join(sources)}")

        analysis_input = {
            "area": req.area,
            "effective_cap": effective_cap,
            "affordability": affordability.model_dump(),
            "homes": [
                {
                    "id": h.id,
                    "address": h.address,
                    "city": h.city,
                    "state": h.state,
                    "price": h.price,
                    "beds": h.beds,
                    "baths": h.baths,
                    "sqft": h.sqft,
                }
                for h in listings[:8]
            ],
        }
        ai_summaries = await ollama_client.summarize_listings(analysis_input)
        if ai_summaries and isinstance(ai_summaries.get("homes"), list):
            by_id = {x.get("id"): x for x in ai_summaries["homes"] if isinstance(x, dict)}
            for h in listings:
                item = by_id.get(h.id)
                if not item:
                    summary, fit, reason = _heuristic_home_summary(h, effective_cap)
                    h.ai_summary = summary
                    h.ai_is_good_choice = fit
                    h.ai_reason = reason
                    continue
                h.ai_summary = item.get("summary") or None
                h.ai_is_good_choice = item.get("is_good_choice")
                h.ai_reason = item.get("reason") or None
            notes.append("AI listing summaries generated by Ollama.")
        else:
            for h in listings:
                summary, fit, reason = _heuristic_home_summary(h, effective_cap)
                h.ai_summary = summary
                h.ai_is_good_choice = fit
                h.ai_reason = reason
            notes.append("Listing summaries used heuristic fallback (Ollama summary unavailable).")
    else:
        notes.append("No listings found under effective budget cap.")

    return SearchResponse(
        area=req.area,
        requested_max_price=req.max_price,
        ai_affordability=affordability,
        effective_price_cap=effective_cap,
        listing_count=len(listings),
        listings=listings,
        notes=notes,
    )


