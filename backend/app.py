async def search_homes(req: SearchRequest) -> SearchResponse:
    return await _run_search(req)


@app.post("/api/listing-details", response_model=ListingDetailResponse)
async def listing_details(req: ListingDetailRequest) -> ListingDetailResponse:
    details = await scapling_client.fetch_listing_details(req.listing_url)
    description = details.get("description")
    photos = details.get("photos") or []
    realtor = RealtorInfo(**(details.get("realtor") or {}))

    ai_payload = {
        "home": {
            "address": req.address,
            "city": req.city,
            "state": req.state,
            "price": req.price,
            "beds": req.beds,
            "baths": req.baths,
            "sqft": req.sqft,
            "description": description,
        },
        "budget_context": {
            "effective_price_cap": req.effective_price_cap,
            "monthly_payment_limit": req.monthly_payment_limit,
            "existing_reason": req.ai_reason,
        },
    }
    ai = await ollama_client.assess_listing_details(ai_payload)
    if ai is None:
        ai_is_good = req.price <= req.effective_price_cap
        ai_explanation = (
            "Price is within your effective cap, but review condition, taxes, and total payment."
            if ai_is_good
            else "Price is above your effective cap, so payment risk is likely high."
        )
    else:
        ai_is_good = ai.get("is_good_choice")
        ai_explanation = ai.get("explanation")

    if not realtor.broker_name and req.broker_name:
        realtor.broker_name = req.broker_name

    return ListingDetailResponse(
        listing_url=req.listing_url,
        description=description,
        photos=photos,
        realtor=realtor,
        ai_is_good_choice=ai_is_good,
        ai_explanation=ai_explanation,
    )


